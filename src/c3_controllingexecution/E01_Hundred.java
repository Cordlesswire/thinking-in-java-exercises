/****************** Exercise 1 *****************
 * Write a program that prints values from 1 to 100.
 ************************************************/ 

package c3_controllingexecution;

public class E01_Hundred {

	public static void main(String[] args) {
		for (int i = 1; i < 101; i++) {
			System.out.println(i);
		}

	}

}
